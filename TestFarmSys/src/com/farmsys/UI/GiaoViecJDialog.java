    void insert() {
        NhatKy kh = getForm();
        if (kh == null) {
            return;
        } else {
            try {
                nkdao.insert(kh);

                // this.clearForm();
                MsgBox.alert(this, "Đã thêm thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm thất bại!");
            }
        }
    }

    NhatKy getForm() {
        if (Validation()) {
            NhatKy nv = new NhatKy();

            nv.setTenCV(txttencv.getText());
//            nv.setMaCay(Integer.valueOf(txtgiantrong.getText()));
//            nv.setMaGian(Integer.valueOf(txtgiantrong.getText()));
            nv.setChiTiet(txtGhiChu.getText());
            nv.setNgayBatDau(Date.valueOf(txtNgayBatDau.getText()));
            nv.setNgayKetThuc(Date.valueOf(txtNgayKiemTra.getText()));
            nv.setNhanVien(txtnguoinhanviec.getText());
            nv.setTrangThai(Integer.valueOf(txttrangthai.getText()));
            return nv;
        }
        return null;
    }

    boolean Validation() {
        //Kiểm tra mã nhân viên
        if (txtNgayKiemTra.getText().length() == 0) {

            return false;
        }
        return true;
    }

    void init() {
        setLocationRelativeTo(null);
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }

    void insert() {
        NhanVien nv = getForm();
        if (nv == null) {
            return;
        } else if (dao.selectById(txtMaNV.getText()) == null) {
            String mk2 = new String(txtXacNhanMK.getPassword());
            if (!mk2.equals(nv.getMatKhau())) {
                ThongBao.alert(this, "Xác nhận mật khẩu không chính xác!");
            } else {
                try {
                    dao.insert(nv);
                    this.fillTable();
                    this.clearForm();
                    ThongBao.alert(this, "Đã thêm nhân viên mới thành công!");
                } catch (Exception e) {
                    ThongBao.alert(this, "Thêm nhân viên thất bại!");
                }
            }
        } else {
            ThongBao.alert(this, "Mã nhân viên đã tồn tại!");
        }
    }

    void update() {
        NhanVien nv = getForm();
        if (nv == null) {
            return;
        } else {
            String mk2 = new String(txtXacNhanMK.getPassword());
            if (!mk2.equals(nv.getMatKhau())) {
                ThongBao.alert(this, "Xác nhận mật khẩu không chính xác!");
            } else {
                try {
                    dao.update(nv);
                    this.fillTable();
                    ThongBao.alert(this, "Đã cập nhật thông tin nhân viên thành công!");
                } catch (Exception e) {
                    ThongBao.alert(this, "Cập nhật thông tin nhân viên thất bại!");
                }
            }
        }
    }

    void delete() {
        if (!PhanQuyen.isManager()) {
            ThongBao.alert(this, "Bạn không có quyền xóa nhân viên!");
        } else {
            String manv = txtMaNV.getText();
            if (manv.equals(PhanQuyen.user.getMaNV())) {
                ThongBao.alert(this, "Bạn không được xóa chính bạn!");
            } else if (ThongBao.confirm(this, "Bạn có thực sự muốn xóa nhân viên này?")) {
                try {
                    dao.delete(manv);
                    this.fillTable();
                    this.clearForm();
                    ThongBao.alert(this, "Xóa nhân viên thành công!");
                } catch (Exception e) {
                    ThongBao.alert(this, "Xóa nhân viên thất bại!");
                }
            }
        }
    }

    void clearForm() {
        NhanVien nv = new NhanVien();
        this.setForm(nv);
        this.row = -1;
        this.updateStatus();
    }

    void edit() {
        String manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectById(manv);
        this.setForm(nv);
        tabs.setSelectedIndex(1);
        this.updateStatus();
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblNhanVien.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblNhanVien.getRowCount() - 1;
        this.edit();
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nv : list) {
                Object[] row = {
                    nv.getMaNV(), ("********"), nv.getHoTen(),
                    nv.getVaiTro() ? "Trưởng phòng" : "Nhân viên", nv.getGioiTinh()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            ThongBao.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNV());
        txtMatKhau.setText(nv.getMatKhau());
        txtHoTen.setText(nv.getHoTen());
        txtXacNhanMK.setText(nv.getMatKhau());
        rdoNhanVien.setSelected(!nv.getVaiTro());
        rdoTruongPhong.setSelected(nv.getVaiTro());
        try {
            if (nv.getGioiTinh().equals("Nam")) {
                cbxGioiTinh.setSelectedIndex(0);
            } else if (nv.getGioiTinh().equals("Nữ")) {
                cbxGioiTinh.setSelectedIndex(1);
            } else {
                cbxGioiTinh.setSelectedIndex(2);
            }
        } catch (Exception e) {
           
        }
    }

    NhanVien getForm() {
        if (Validation()) {
            NhanVien nv = new NhanVien();
            nv.setMaNV(txtMaNV.getText());
            nv.setMatKhau(new String(txtMatKhau.getText()));
            nv.setHoTen(txtHoTen.getText());
            nv.setVaiTro(rdoTruongPhong.isSelected());
            nv.setGioiTinh(cbxGioiTinh.getItemAt(cbxGioiTinh.getSelectedIndex()));
            return nv;
        }
        return null;
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);
        //Trạng Thái Form
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        //Trạng Thái Điều Hướng
        btnDauTien.setEnabled(edit && !first);
        btnLui.setEnabled(edit && !first);
        btnTiepTheo.setEnabled(edit && !last);
        btnCuoiCung.setEnabled(edit && !last);
    }

    boolean Validation() {
        //Kiểm tra mã nhân viên
        if (txtMaNV.getText().length() == 0) {
            ThongBao.alert(this, "Bạn chưa nhập mã nhân viên!");
            txtMaNV.setFocusable(true);
            return false;
        } else if (txtMaNV.getText().length() > 50) {
            ThongBao.alert(this, "Mã nhân viên không vượt quá 50 ký tự!");
            txtMaNV.setFocusable(true);
            return false;
        }

        //Kiểm tra họ tên
        if (txtHoTen.getText().length() == 0) {
            ThongBao.alert(this, "Bạn chưa nhập họ tên cho nhân viên!");
            txtHoTen.setFocusable(true);
            return false;
        }

        //Kiểm tra mật khẩu
        if (txtMatKhau.getText().length() == 0) {
            ThongBao.alert(this, "Bạn chưa nhập mật khẩu cho nhân viên!");
            txtMatKhau.setFocusable(true);
            return false;
        }

        //Kiểm tra xác nhận mật khẩu
        if (txtXacNhanMK.getText().length() == 0) {
            ThongBao.alert(this, "Bạn chưa nhập xác nhận mật khẩu cho nhân viên!");
            txtXacNhanMK.setFocusable(true);
            return false;
        }
        return true;
    }

    private void openThongKe(int index) {
        if (PhanQuyen.isLogin()) {
            if (index == 3 && !PhanQuyen.isManager()) {
                ThongBao.alert(this, "Bạn không có quyền xem thông tin doanh thu!");
            } else {
                ThongKeJFrame tkwin = new ThongKeJFrame();
                tkwin.setVisible(true);
                tkwin.selectTab(index);
            }
        } else {
            ThongBao.alert(this, "Vui lòng đăng nhập!");
        }
    }


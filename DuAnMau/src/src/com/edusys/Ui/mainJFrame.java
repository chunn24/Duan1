    private void init() {
        this.setIconImage(Img.getAppIcon());
        this.setLocationRelativeTo(null);
        new ChaoJDialog(this, true).setVisible(true);
        new DangNhapJDialog(this, true).setVisible(true);
        loadLbl();
        new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date now = new Date();
                SimpleDateFormat formater = new SimpleDateFormat("hh:mm:ss a");
                String text = formater.format(now);
                lblDongHo.setText(text);
            }
        }).start();
    }

    private void openDoiMatKhau() {
        if (PhanQuyen.isLogin()) {
            new DoiMatKhauJDialog(this, true).setVisible(true);
        } else {
            ThongBao.alert(this, "Vui lòng đăng nhập!");
        }
    }

    private void openDangXuat() {
        int ask = JOptionPane.showConfirmDialog(this, "Bạn có chắc là mình muốn đăng xuất ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (ask == 0) {
            PhanQuyen.clear();
            new DangNhapJDialog(this, true).setVisible(true);
            loadLbl();
        }
    }


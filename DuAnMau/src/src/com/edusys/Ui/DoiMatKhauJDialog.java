    private void init(){
        this.setLocationRelativeTo(null);
        txtMaNV.setText(PhanQuyen.user.getMaNV());
        txtMaNV.enable(false);
    }
    private void DoiMatKhau(){
        if(Validation()){
            String matKhauCu = new String(txtMatKhauCu.getText());
            String matKhauMoi = new String(txtMatKhauMoi.getText());
            String xacNhan = new String(txtXacNhan.getText());
            if(!matKhauCu.equals(PhanQuyen.user.getMatKhau())){
                ThongBao.alert(this, "Sai mật khẩu cũ!");
            }else if(!matKhauMoi.equals(xacNhan)){
                ThongBao.alert(this, "Xác nhận mật khẩu không khớp!");
            }else{
                PhanQuyen.user.setMatKhau(matKhauMoi);
                dao.update(PhanQuyen.user);
                ThongBao.alert(this, "Đổi mật khẩu thành công!");
            }
        }
    }
    private void huyBo(){
        this.dispose();
    }
    boolean Validation(){
        //kiểm tra để trống
        if(txtMatKhauCu.getText().length() == 0){
            ThongBao.alert(this, "Bạn chưa nhập mật khẩu cũ!");
            txtMatKhauCu.setFocusable(true);
            return false;
        }
        if(txtMatKhauMoi.getText().length() == 0){
            ThongBao.alert(this, "Bạn chưa nhập mật khẩu mới!");
            txtMatKhauMoi.setFocusable(true);
            return false;
        }
        if(txtXacNhan.getText().length() == 0){
            ThongBao.alert(this, "Bạn chưa nhập xác nhận mật khẩu!");
            txtXacNhan.setFocusable(true);
            return false;
        }
        return true;
    }

    void fillComboBoxChuyenDe(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboChuyenDe.getModel();
        model.removeAllElements();
        List<ChuyenDe> list = cddao.selectAll();
        for(ChuyenDe cd : list){
            model.addElement(cd);
        }
        this.fillComboBoxKhoaHoc();
    }
    void fillComboBoxKhoaHoc(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboKhoaHoc.getModel();
        model.removeAllElements();
        ChuyenDe chuyenDe = (ChuyenDe) cboChuyenDe.getSelectedItem();
        if(chuyenDe != null){
            List<String> list = khdao.selectByChuyenDe_MaKH(chuyenDe.getMaCD());
            for(String kh : list){
                model.addElement(kh);
            }
            this.fillTableHocVien();
        }
    }
    void fillTableHocVien(){
        DefaultTableModel model = (DefaultTableModel) tblHocVien.getModel();
        model.setRowCount(0);
        String str = (String)(cboKhoaHoc.getSelectedItem());
        if(str != null){
            int index = str.indexOf(".");
            int khoahoc = Integer.parseInt(str.substring(0, index));
        if(khoahoc > 0){
            List<HocVien> list = hvdao.selectByKhoaHoc(khoahoc);
            for(int i = 0; i < list.size(); i++){
                HocVien hv = list.get(i);
                String hoten = nhdao.selectById(hv.getMaNH()).getHoTen();
                model.addRow(new Object[]{i + 1, hv.getMaHV(), hv.getMaNH(), hoten, hv.getDiem()});
            }
            this.fillTableNguoiHoc();
        }
        }
        
    }
 
   
    private void sendDiem(String manh, float diem) {
        String email = nhdao.selectById(manh).getEmail();
        String hoTen = nhdao.selectById(manh).getHoTen();
        String chuyenDe = cboChuyenDe.getSelectedItem().toString();

        try {
            Properties p = new Properties();
            p.put("mail.smtp.auth", "true");
            p.put("mail.smtp.starttls.enable", "true");
            p.put("mail.smtp.host", "smtp.gmail.com");
            p.put("mail.smtp.port", 587);
            Session s = Session.getInstance(p,
                    new javax.mail.Authenticator() {
                @Override
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(accountName, accountPassword);
                }
            });
            String from = accountName;
            String to = email;
            String subject = "Thông báo điểm";
            String body = "Chào: " + hoTen + ", điểm môn " + chuyenDe + " là: " + diem;

            Message msg = new MimeMessage(s);
            msg.setFrom(new InternetAddress(from));
            msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
            msg.setSubject(subject);
            msg.setText(body);
            msg.setSentDate(new Date());

            Transport.send(msg);
//            MsgBox.alert(this, "Một email chứa mã OTP đã gửi vào email của bạn!");

        } catch (MessagingException ex) {
            System.out.println(ex);
        }
    }
    void fillTableNguoiHoc(){
        DefaultTableModel model = (DefaultTableModel) tblNguoiHoc.getModel();
        model.setRowCount(0);
        String str = (String)(cboKhoaHoc.getSelectedItem());
        if(str != null){
            int index = str.indexOf(".");
            int khoaHoc = Integer.parseInt(str.substring(0, index));
        String keyword = txtTimKiem.getText();
        List<NguoiHoc> list = nhdao.selectNotInCourse(khoaHoc, keyword);
        for(NguoiHoc nh : list){
            model.addRow(new Object[]{
                nh.getMaNH(), nh.getHoTen(), nh.getGioiTinh() ? "Nam" : "Nữ",
                nh.getNgaySinh(), nh.getDienThoai(), nh.getEmail()
            });
        }
        }
        
    }
    void removeHocVien(){
        if(!PhanQuyen.isManager()){
            ThongBao.alert(this, "Bạn không có quyền xóa học viên");
        }else{
            if(ThongBao.confirm(this, "Bạn có chắc là mình muốn xóa các học viên được chọn?")){
                for(int row : tblHocVien.getSelectedRows()){
                    int mahv = (Integer) tblHocVien.getValueAt(row, 1);
                    hvdao.delete(String.valueOf(mahv));
                }
                this.fillTableHocVien();
            }
            ThongBao.alert(this, "Xóa học viên thành công!");
        }
    }
    void addHocVien(){
        String str = (String)(cboKhoaHoc.getSelectedItem());
        if(str != null){
            int index = str.indexOf(".");
            int khoaHoc = Integer.parseInt(str.substring(0, index));
        for(int row : tblNguoiHoc.getSelectedRows()){
            HocVien hv = new HocVien();
            hv.setMaKH(khoaHoc);
            hv.setDiem(Double.valueOf(0));
            hv.setMaNH((String) tblNguoiHoc.getValueAt(row, 0));
            hvdao.insert(hv);
        }
        ThongBao.alert(this, "Thêm thành công người học vào khóa học đã chọn!");
        this.fillTableHocVien();
        this.tabs.setSelectedIndex(0);
    }
    }
    void updateHocVien(){
        for(int i = 0; i < tblHocVien.getRowCount(); i++){
            int mahv = (Integer) tblHocVien.getValueAt(i, 1);
            HocVien hv = hvdao.selectById(String.valueOf(mahv));
            
            if(tblHocVien.getValueAt(i, 4) == null || tblHocVien.getValueAt(i, 4).toString().equals("")){
                ThongBao.alert(this, "Không được để trống điểm");
                return;
            }else{
                
                if(checkDiem((Double)tblHocVien.getValueAt(i, 4))){
                    float diem = Float.parseFloat(tblHocVien.getValueAt(1, 4).toString());
                    hv.setDiem((Double) tblHocVien.getValueAt(i, 4));
                    hvdao.update(hv);
                    String manh = (String) tblHocVien.getValueAt(i, 2);
                    this.sendDiem(manh, diem);
                }else{
                    String hoten = nhdao.selectById(hv.getMaNH()).getHoTen();
                    ThongBao.alert(this, "Điểm của học viên "+ hoten + " không hợp lệ!");
                    return;
                }
            }
        }
        ThongBao.alert(this, "Bạn đã cập nhật điểm thành công");
    }
    boolean checkDiem(double diem){
        if(diem < 0 || diem >10){
            return false;
        }else{
            return true;
        }
    }
